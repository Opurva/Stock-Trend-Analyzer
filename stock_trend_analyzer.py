# Import libraries
import yfinance as yf
import pandas as pd
import matplotlib.pyplot as plt

# Choose stock (example: Apple)
stock = "AAPL"

# Download data
data = yf.download(stock, start="2024-01-01", end="2025-01-01")

# Show first 5 rows
data.head()

plt.figure()
plt.plot(data['Close'])
plt.title(f"{stock} Closing Price")
plt.xlabel("Date")
plt.ylabel("Price")
plt.show()

# Calculate Moving Averages
data['MA_5'] = data['Close'].rolling(window=5).mean()
data['MA_20'] = data['Close'].rolling(window=20).mean()

# Plot
plt.figure()
plt.plot(data['Close'], label="Close Price")
plt.plot(data['MA_5'], label="5-Day MA")
plt.plot(data['MA_20'], label="20-Day MA")

plt.title(f"{stock} Trend Analysis")
plt.legend()
plt.show()

# Get last values
last_ma5 = data['MA_5'].iloc[-1]
last_ma20 = data['MA_20'].iloc[-1]

if last_ma5 > last_ma20:
    print("📈 Uptrend (Bullish)")
elif last_ma5 < last_ma20:
    print("📉 Downtrend (Bearish)")
else:
    print("😐 Sideways Trend")

# Calculate RSI
delta = data['Close'].diff()

gain = (delta.where(delta > 0, 0)).rolling(window=14).mean()
loss = (-delta.where(delta < 0, 0)).rolling(window=14).mean()

rs = gain / loss
data['RSI'] = 100 - (100 / (1 + rs))

# Plot RSI
plt.figure()
plt.plot(data['RSI'])
plt.axhline(70)
plt.axhline(30)
plt.title("RSI Indicator")
plt.show()

last_rsi = data['RSI'].iloc[-1]

if last_rsi > 70:
    print("🔴 Overbought → Sell Signal")
elif last_rsi < 30:
    print("🟢 Oversold → Buy Signal")
else:
    print("⚪ Neutral")



