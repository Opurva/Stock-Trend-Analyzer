import streamlit as st
import yfinance as yf
import pandas as pd
import matplotlib.pyplot as plt

# Title
st.title("📊 Stock Trend Analyzer")

# Input stocks
stocks = st.text_input("Enter stock symbols (comma separated)", "AAPL,MSFT,GOOGL")
stock_list = [s.strip() for s in stocks.split(",")]

# Date range
start_date = st.date_input("Start Date", pd.to_datetime("2022-01-01"))
end_date = st.date_input("End Date", pd.to_datetime("2024-01-01"))

# Fetch data
if st.button("Analyze"):
    data = yf.download(stock_list, start=start_date, end=end_date)['Close']

    st.subheader("📈 Price Comparison")
    st.line_chart(data)

    # Normalize
    normalized = data / data.iloc[0] * 100

    st.subheader("⚖️ Normalized Comparison (Performance)")
    st.line_chart(normalized)

    # Trend logic
    st.subheader("🧠 Trend Analysis")

    for stock in stock_list:
        ma5 = data[stock].rolling(5).mean().iloc[-1]
        ma20 = data[stock].rolling(20).mean().iloc[-1]

        if ma5 > ma20:
            st.write(f"{stock}: 📈 Uptrend")
        elif ma5 < ma20:
            st.write(f"{stock}: 📉 Downtrend")
        else:
            st.write(f"{stock}: 😐 Sideways")

    # Best performer
    returns = normalized.iloc[-1]
    best_stock = returns.idxmax()

    st.success(f"🏆 Best Performing Stock: {best_stock}")



